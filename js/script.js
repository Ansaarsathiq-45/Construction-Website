function toggleChat() {
   const chatbox = document.getElementById('chatbox');
   chatbox.style.display = (chatbox.style.display === 'none') ? 'block' : 'none';
 }
 
 function sendMessage() {
   const userInput = document.getElementById('user-input').value;
   const chatLog = document.getElementById('chat-log');
   chatLog.innerHTML += `<div>User: ${userInput}</div>`;
   document.getElementById('user-input').value = '';
   
   // Simple response logic based on construction-related queries
   setTimeout(() => {
     chatLog.innerHTML += `<div>Bot: ${getBotResponse(userInput)}</div>`;
   }, 1000);
 }
 
 function getBotResponse(input) {
   if (input.toLowerCase().includes('quote')) {
     return 'For a construction quote, please contact our office at [phone number] or email [email address].';
   }
   if (input.toLowerCase().includes('services')) {
     return 'We offer services in residential and commercial construction, renovations, and project management.';
   }
   return 'I am a simple bot. Ask me about our services or contact details!';
 }
 
